"use client";

import { useEffect, useState, useMemo } from "react";
import { ChevronLeftIcon, ChevronRightIcon } from "@heroicons/react/24/solid";

import ReadBlockEditor from "./readBlockEditor";
import { Button, useDisclosure } from "@heroui/react";
import { useRouter } from "next/navigation";
import { Note } from "@/store/editorSotre";
import { useNoteStore } from "@/components/providers/editor-provider";
import { useCachedSession } from "@/app/hooks/user/useCachedSession";
import {
  canAccessContent,
  getAccessMessage,
  getRequiredRole,
} from "@/lib/utils/access-control";
import SimpleModal from "@/components/modal/simpleModal";

interface GroupedNotes {
  [key: string]: Note[];
}

type UserRole = "guest" | "user" | "supporter" | "admin";

// Î†àÎ≤® ÏÑ§Ï†ï Î∞è ÏïÑÏù¥ÏΩò Ïª¥Ìè¨ÎÑåÌä∏
const LEVEL_CONFIG = {
  BEGINNER: {
    icon: "üü¢",
    label: "Ï¥àÍ∏â",
    bgColor: "bg-green-50",
    borderColor: "border-green-200",
    textColor: "text-green-800",
  },
  INTERMEDIATE: {
    icon: "üü°",
    label: "Ï§ëÍ∏â",
    bgColor: "bg-yellow-50",
    borderColor: "border-yellow-200",
    textColor: "text-yellow-800",
  },
  ADVANCED: {
    icon: "üî¥",
    label: "Í≥†Í∏â",
    bgColor: "bg-red-50",
    borderColor: "border-red-200",
    textColor: "text-red-800",
  },
};

// Î†àÎ≤® ÏàúÏÑú Ï†ïÏùò
const LEVEL_ORDER: ("BEGINNER" | "INTERMEDIATE" | "ADVANCED")[] = [
  "BEGINNER",
  "INTERMEDIATE",
  "ADVANCED",
];

// Í∂åÌïú ÏïÑÏù¥ÏΩò Ïª¥Ìè¨ÎÑåÌä∏
const ContentLevelIcon = ({
  level,
}: {
  level: "BEGINNER" | "INTERMEDIATE" | "ADVANCED";
}) => {
  return <span className="mr-1">{LEVEL_CONFIG[level].icon}</span>;
};

export default function NoteItemView({
  fetchNotes,
  initialNote,
}: {
  fetchNotes: Note[];
  initialNote: Note | null;
}) {
  const router = useRouter();
  const { session, isAuthenticated } = useCachedSession();
  const { isOpen, onOpen, onOpenChange } = useDisclosure();

  const [note, setNote] = useState<Note | null>(initialNote);
  const [isSidebarOpen, setIsSidebarOpen] = useState(false);
  const [accessDeniedInfo, setAccessDeniedInfo] = useState<{
    contentLevel: "BEGINNER" | "INTERMEDIATE" | "ADVANCED";
    userRole: UserRole;
    title: string;
    content: string;
    confirmText: string;
    confirmAction: () => void;
    showCancel: boolean;
    confirmColor: "primary" | "danger" | "success" | "warning";
  } | null>(null);

  // üî• ÏÇ¨Ïö©Ïûê Ïó≠Ìï† Í≤∞Ï†ï (Í≤åÏä§Ìä∏ Í∏∞Î≥∏Í∞í)
  const userRole: UserRole = useMemo(() => {
    if (!isAuthenticated || !session?.user) {
      return "guest";
    }

    switch (session.user.role) {
      case "admin":
        return "admin";
      case "supporter":
        return "supporter";
      case "user":
        return "user";
      default:
        return "guest";
    }
  }, [isAuthenticated, session?.user]);

  // üî• Î†àÎ≤® Í∏∞Î∞ò Í∑∏Î£πÌïë (Î†àÎ≤® -> ÏÑúÎ∏åÏπ¥ÌÖåÍ≥†Î¶¨ -> ÎÖ∏Ìä∏)
  const groupedNotesByLevel = useMemo(() => {
    if (!fetchNotes || fetchNotes.length === 0) {
      return {};
    }

    return fetchNotes.reduce(
      (acc, note) => {
        const level = note.level || "BEGINNER";
        const subCategoryName = note.subCategory?.name || "Uncategorized";

        if (!acc[level]) {
          acc[level] = {};
        }
        if (!acc[level][subCategoryName]) {
          acc[level][subCategoryName] = [];
        }
        acc[level][subCategoryName].push(note);

        return acc;
      },
      {} as Record<string, GroupedNotes>,
    );
  }, [fetchNotes]);

  // üî• ÏÑúÎ∏åÏπ¥ÌÖåÍ≥†Î¶¨ Í∏∞Î∞ò Í∑∏Î£πÌïë (ÏÇ¨Ïù¥ÎìúÎ∞îÏö©)
  const groupedNotes = useMemo(() => {
    if (!fetchNotes || fetchNotes.length === 0) {
      return {};
    }

    return fetchNotes.reduce((acc, note) => {
      const subCategoryName = note.subCategory?.name || "Uncategorized";

      if (!acc[subCategoryName]) {
        acc[subCategoryName] = [];
      }
      acc[subCategoryName].push(note);

      return acc;
    }, {} as GroupedNotes);
  }, [fetchNotes]);

  const { setContent } = useNoteStore((state) => state);

  useEffect(() => {
    if (note) {
      setContent({
        noteId: note.noteId,
        title: note.title,
        content: note.content,
      });
    }
  }, [note, setContent]);

  // üî• Î™®Îã¨ ÏÑ§Ï†ï ÏÉùÏÑ± Ìï®Ïàò
  const createModalConfig = (
    contentLevel: "BEGINNER" | "INTERMEDIATE" | "ADVANCED",
    userRole: UserRole,
  ) => {
    switch (contentLevel) {
      case "INTERMEDIATE":
        if (userRole === "guest") {
          return {
            title: "üîí Ï§ëÍ∏â ÏΩòÌÖêÏ∏†",
            content:
              "Ï§ëÍ∏â ÏΩòÌÖêÏ∏†Îäî ÌöåÏõêÍ∞ÄÏûÖÏù¥ ÌïÑÏöîÌï©ÎãàÎã§.\n\nÎ¨¥Î£åÎ°ú Í∞ÄÏûÖÌïòÏãúÎ©¥ Ï§ëÍ∏â ÏΩòÌÖêÏ∏†ÍπåÏßÄ Î™®Îëê Ïù¥Ïö©ÌïòÏã§ Ïàò ÏûàÏäµÎãàÎã§.",
            confirmText: "Î°úÍ∑∏Ïù∏ / ÌöåÏõêÍ∞ÄÏûÖ",
            confirmAction: () => {
              onOpenChange();
              router.push("/signin");
            },
            showCancel: true,
            confirmColor: "primary" as const,
          };
        } else {
          return {
            title: "üîí Ï§ëÍ∏â ÏΩòÌÖêÏ∏†",
            content:
              "Ïù¥ÎØ∏ ÌöåÏõêÏù¥ÏãúÍµ∞Ïöî!\n\nÌïòÏßÄÎßå Ïù¥ ÏΩòÌÖêÏ∏†Ïóê Ï†ëÍ∑ºÌï† Ïàò ÏóÜÏäµÎãàÎã§. Í∂åÌïúÏùÑ ÌôïÏù∏Ìï¥Ï£ºÏÑ∏Ïöî.",
            confirmText: "ÌôïÏù∏",
            confirmAction: () => onOpenChange(),
            showCancel: false,
            confirmColor: "primary" as const,
          };
        }

      case "ADVANCED":
        return {
          title: "üíé Í≥†Í∏â ÏΩòÌÖêÏ∏†",
          content:
            "Í≥†Í∏â ÏΩòÌÖêÏ∏†Îäî ÌõÑÏõêÏûê Ï†ÑÏö©ÏûÖÎãàÎã§.\n\nÌõÑÏõêÌï¥Ï£ºÏãúÎ©¥ Î™®Îì† ÌîÑÎ¶¨ÎØ∏ÏóÑ ÏΩòÌÖêÏ∏†Î•º Ïù¥Ïö©ÌïòÏã§ Ïàò ÏûàÏäµÎãàÎã§.",
          confirmText: "ÌõÑÏõêÌïòÍ∏∞",
          confirmAction: () => {
            onOpenChange();
            router.push("/support");
          },
          showCancel: true,
          confirmColor: "warning" as const,
        };

      default:
        return {
          title: "üö´ Ï†ëÍ∑º Ï†úÌïú",
          content: "Ïù¥ ÏΩòÌÖêÏ∏†Ïóê Ï†ëÍ∑ºÌï† Í∂åÌïúÏù¥ ÏóÜÏäµÎãàÎã§.",
          confirmText: "ÌôïÏù∏",
          confirmAction: () => onOpenChange(),
          showCancel: false,
          confirmColor: "primary" as const,
        };
    }
  };

  // üî• ÎÖ∏Ìä∏ ÌÅ¥Î¶≠ Ìï∏Îì§Îü¨ (Í∂åÌïú Ï≤¥ÌÅ¨ Ìè¨Ìï®)
  const handleNoteClick = (selectedNote: Note) => {
    const hasAccess = canAccessContent(
      userRole,
      selectedNote.level || "BEGINNER",
    );

    if (hasAccess) {
      setNote(selectedNote);
    } else {
      // Í∂åÌïú ÏóÜÏùÑ Ïãú Î™®Îã¨ ÏÑ§Ï†ï ÌõÑ ÌëúÏãú
      const modalConfig = createModalConfig(
        selectedNote.level || "BEGINNER",
        userRole,
      );
      setAccessDeniedInfo({
        contentLevel: selectedNote.level || "BEGINNER",
        userRole,
        ...modalConfig,
      });
      onOpen();
    }
  };

  const toggleSidebar = () => setIsSidebarOpen(!isSidebarOpen);

  return (
    <div className="flex relative w-full min-h-screen">
      {/* Sidebar */}
      <div
        className={`fixed left-0 top-0 h-full bg-gray-100 transition-all duration-300 ease-in-out ${isSidebarOpen ? "w-64" : "w-0"} flex flex-col z-10`}
      >
        {isSidebarOpen && (
          <>
            <div className="flex-grow overflow-y-auto">
              <div className="p-4">
                <h2 className="text-black font-bold text-[25px] mb-4">
                  {note?.mainCategory}
                </h2>
                <h3 className="text-black font-bold text-[20px] mb-2">Î™©Ï∞®</h3>
                {LEVEL_ORDER.map((level) => {
                  const levelData = groupedNotesByLevel[level];

                  if (!levelData) return null;

                  return (
                    <div key={level} className="mb-6">
                      {/* Î†àÎ≤® Ìó§Îçî */}
                      <div
                        className={`flex items-center mb-3 p-2 rounded-lg ${LEVEL_CONFIG[level].bgColor} ${LEVEL_CONFIG[level].borderColor} border`}
                      >
                        <ContentLevelIcon level={level} />
                        <h4
                          className={`font-bold text-lg ${LEVEL_CONFIG[level].textColor}`}
                        >
                          {LEVEL_CONFIG[level].label}
                        </h4>
                      </div>

                      {/* ÏÑúÎ∏åÏπ¥ÌÖåÍ≥†Î¶¨Î≥Ñ ÎÖ∏Ìä∏ */}
                      {Object.entries(levelData).map(
                        ([subCategoryName, notes]) => (
                          <div
                            key={`${level}-${subCategoryName}`}
                            className="mb-4 ml-2"
                          >
                            <h5 className="text-black font-semibold mb-2 text-sm">
                              {subCategoryName}
                            </h5>
                            <ul className="ml-3">
                              {notes.map((noteItem, index) => {
                                const hasAccess = canAccessContent(
                                  userRole,
                                  noteItem.level || "BEGINNER",
                                );
                                const isCurrentNote =
                                  note?.noteId === noteItem.noteId;

                                return (
                                  <li
                                    key={noteItem.noteId || index}
                                    className={`mb-2 cursor-pointer flex items-center transition-colors text-sm
    ${isCurrentNote
                                        ? 'text-blue-600 font-semibold'
                                        : hasAccess
                                          ? 'text-black hover:text-gray-600'
                                          : 'text-gray-400 hover:text-gray-500'
                                      }
    ${!hasAccess ? 'opacity-70' : ''}
  `}
                                    title={!hasAccess ? getAccessMessage(userRole, noteItem.level || 'BEGINNER') : ''}
                                    onClick={() => handleNoteClick(noteItem)}
                                  >
                                    <span className={`${!hasAccess ? 'line-through' : ''}`}>
                                      {noteItem.title}
                                    </span>
                                    {!hasAccess && (
                                      <span className="ml-1 text-xs">üîí</span>
                                    )}
                                  </li>
                                );
                              })}
                            </ul>
                          </div>
                        ),
                      )}
                    </div>
                  );
                })}

                {/* üî• ÏÇ¨Ïö©Ïûê Í∂åÌïú Ï†ïÎ≥¥ ÌëúÏãú */}
                <div className="mt-6 p-3 bg-gray-50 rounded-lg border">
                  <div className="text-xs text-gray-600 mb-2">ÌòÑÏû¨ Í∂åÌïú</div>
                  <div className="flex items-center gap-2">
                    <div
                      className={`px-2 py-1 rounded text-xs font-medium ${userRole === "admin"
                          ? "bg-purple-100 text-purple-800"
                          : userRole === "supporter"
                            ? "bg-yellow-100 text-yellow-800"
                            : userRole === "user"
                              ? "bg-blue-100 text-blue-800"
                              : "bg-gray-100 text-gray-800"
                        }`}
                    >
                      {userRole === "admin"
                        ? "Í¥ÄÎ¶¨Ïûê"
                        : userRole === "supporter"
                          ? "ÌõÑÏõêÏûê"
                          : userRole === "user"
                            ? "ÌöåÏõê"
                            : "Í≤åÏä§Ìä∏"}
                    </div>
                  </div>
                  <div className="text-xs text-gray-500 mt-1">
                    {userRole === "admin"
                      ? "Î™®Îì† ÏΩòÌÖêÏ∏† Ï†ëÍ∑º Í∞ÄÎä•"
                      : userRole === "supporter"
                        ? "üü¢üü°üî¥ Î™®Îì† Î†àÎ≤® Ï†ëÍ∑º Í∞ÄÎä•"
                        : userRole === "user"
                          ? "üü¢üü° Ï¥àÍ∏â, Ï§ëÍ∏â Ï†ëÍ∑º Í∞ÄÎä•"
                          : "üü¢ Ï¥àÍ∏âÎßå Ï†ëÍ∑º Í∞ÄÎä•"}
                  </div>
                </div>
              </div>
            </div>
            <div className="p-3 mb-[100px]">
              <Button
                className="w-full bg-slate-500"
                onClick={() => router.back()}
              >
                Îí§Î°úÍ∞ÄÍ∏∞
              </Button>
            </div>
          </>
        )}
      </div>

      {/* Main Content */}
      <div
        className={`w-full min-h-screen transition-all duration-300 ease-in-out ${isSidebarOpen ? "ml-64" : "ml-0"}`}
      >
        <button
          className="m-2 bg-slate-700 hover:bg-slate-500 text-white p-2 rounded-[5px]"
          onClick={toggleSidebar}
        >
          {isSidebarOpen ? (
            <ChevronLeftIcon className="w-6 h-6" />
          ) : (
            <ChevronRightIcon className="w-6 h-6" />
          )}
        </button>
        <ReadBlockEditor note={note!} />
      </div>

      {/* üî• Ïû¨ÏÇ¨Ïö© Í∞ÄÎä•Ìïú SimpleModal ÏÇ¨Ïö© */}
      {accessDeniedInfo && (
        <SimpleModal
          confirmColor={accessDeniedInfo.confirmColor}
          confirmText={accessDeniedInfo.confirmText}
          content={accessDeniedInfo.content}
          isOpen={isOpen}
          showCancel={accessDeniedInfo.showCancel}
          title={accessDeniedInfo.title}
          onCancel={() => onOpenChange()}
          onConfirm={accessDeniedInfo.confirmAction}
          onOpenChange={onOpenChange}
        />
      )}
    </div>
  );
}
